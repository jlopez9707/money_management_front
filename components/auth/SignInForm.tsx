"use client"

import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import {
    Form,
    FormControl,
    FormField,
    FormItem,
    FormLabel,
    FormMessage,
} from "@/components/ui/form";

import * as z from "zod";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";

import { useState } from "react";
import { LoaderCircle } from "lucide-react";
import toast from "react-hot-toast";
import { AuthFormProps } from "./AuthForm";
import { login } from "@/actions/auth/auth";
import { useRouter } from "next/navigation";

const SignInForm = ({ setTypeSelected }: AuthFormProps) => {

    const router = useRouter();
    const [isLoading, setisLoading] = useState<boolean>(false)

    // ============ Form ============
    const formSchema = z.object({
        email: z.string().trim().email('Por favor ingresa un correo válido. Ejemplo: user@mail.com').min(1, {
            message: 'Este campo es requerido'
        }),
        password: z.string().min(6, {
            message: 'La contraseña debe tener al menos 6 caracteres'
        })
    })

    const form = useForm<z.infer<typeof formSchema>>({
        resolver: zodResolver(formSchema),
        defaultValues: {
            email: '',
            password: ''
        }
    })

    const { handleSubmit, control } = form;

    // ============ Sign In ===========
    const onSubmit = async (data: z.infer<typeof formSchema>) => {
        setisLoading(true);

        try {
            const res = await login(data);

            if (res.success) {
                toast.success('Sesión iniciada correctamente', { duration: 2500 });
                router.push('/dashboard');
            } else {
                const errorMessage = res.message;
                if (errorMessage.toLowerCase().includes('invalid login credentials')) {
                    toast.error('Credenciales incorrectas', { duration: 4000 });
                } else if (errorMessage.toLowerCase().includes('rate limit')) {
                    toast.error('Demasiados intentos. Por favor espera unos minutos.', { duration: 5000 });
                } else if (errorMessage.toLowerCase().includes('invalid') || errorMessage.toLowerCase().includes('email')) {
                    toast.error('El correo electrónico no es válido. Asegúrate de que no tenga espacios adicionales.', { duration: 4000 });
                } else {
                    toast.error(errorMessage || 'Error al iniciar sesión', { duration: 4000 });
                }
            }

        } catch (error) {
            const message = error instanceof Error ? error.message : "Error desconocido";
            toast.error(message, { duration: 2500 });
        } finally {
            setisLoading(false);
        }
    }

    const githubSignIn = async () => {

    }

    const googleSignIn = async () => {

    }

    return (
        <div>
            <div className="w-full backdrop-blur-xl py-2 rounded-4xl">
                <div className="text-center">
                    <h1 className="lg:text-5xl md:text-4xl text-3xl font-semibold text-center my-4">Iniciar Sesión</h1>
                    <p className="text-sm text-muted-foreground mb-8">
                        Ingresa para acceder a todo el contenido
                    </p>
                </div>

                <Form {...form}>
                    <form onSubmit={handleSubmit(onSubmit)} className="mx-4">
                        <div className="grid gap-2">
                            {/* ========== Email ========= */}
                            <FormField
                                control={control}
                                name="email"
                                render={({ field }) => (
                                    <FormItem className="mb-3">
                                        <FormLabel>Correo</FormLabel>
                                        <FormControl>
                                            <Input
                                                {...field}
                                                id="email"
                                                placeholder="name@example.com"
                                                type="email"
                                                autoComplete="email"
                                                disabled={isLoading}
                                            />
                                        </FormControl>
                                        <FormMessage />
                                    </FormItem>
                                )}
                            />

                            {/* ========== Password ========= */}
                            <FormField
                                control={control}
                                name="password"
                                render={({ field }) => (
                                    <FormItem className="mb-3">
                                        <FormLabel>Contraseña</FormLabel>
                                        <FormControl>
                                            <Input
                                                {...field}
                                                id="password"
                                                placeholder="*****"
                                                type="password"
                                                autoComplete="current-password"
                                                disabled={isLoading}
                                            />
                                        </FormControl>
                                        <FormMessage />
                                    </FormItem>
                                )}
                            />

                            <button
                                type="button"
                                onClick={() => setTypeSelected('recover-password')}
                                className="underline text-foreground underline-offset-4 hover:text-primary mb-6 text-sm text-end cursor-pointer bg-transparent border-none p-0 ml-auto block"
                            >
                                ¿Olvidaste tu contraseña?
                            </button>

                            {/* ========== Submit ========= */}
                            <Button type="submit" disabled={isLoading}>
                                {isLoading && (
                                    <LoaderCircle className="mr-2 h-4 w-4 animate-spin" />
                                )}
                                Ingresar
                            </Button>


                            {/* ========== Separador ========= */}
                            <div className="relative my-3">
                                <div className="absolute inset-0 flex items-center">
                                    <span className="w-full border-t" />
                                </div>
                                <div className="relative flex justify-center text-xs uppercase">
                                    <span className="px-2 text-muted-foreground">
                                        O continúa con
                                    </span>
                                </div>
                            </div>

                            {/* ========== Botones OAuth ========= */}
                            <div className="grid grid-cols-2 gap-3">
                                {/* Botón Google */}
                                <Button
                                    variant="outline"
                                    type="button"
                                    onClick={googleSignIn}
                                    disabled={isLoading}
                                    className="flex items-center justify-center gap-2 cursor-pointer"
                                >
                                    <svg className="h-4 w-4" viewBox="0 0 24 24">
                                        <path
                                            d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                                            fill="#4285F4"
                                        />
                                        <path
                                            d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                                            fill="#34A853"
                                        />
                                        <path
                                            d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                                            fill="#FBBC05"
                                        />
                                        <path
                                            d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                                            fill="#EA4335"
                                        />
                                    </svg>
                                    Google
                                </Button>

                                {/* Botón Github */}
                                <Button
                                    variant="outline"
                                    type="button"
                                    onClick={githubSignIn}
                                    disabled={isLoading}
                                    className="flex items-center justify-center gap-2 cursor-pointer"
                                >
                                    <svg className="h-4 w-4" fill="currentColor" viewBox="0 0 24 24">
                                        <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z" />
                                    </svg>
                                    Github
                                </Button>
                            </div>
                        </div>
                    </form>
                </Form>

                {/* ========== Sign Up ========= */}
                <p className="text-center text-sm text-foreground mt-4">
                    {"¿No tienes cuenta?  "}
                    <button
                        type="button"
                        onClick={() => setTypeSelected('sign-up')}
                        className="underline underline-offset-4 hover:text-primary cursor-pointer bg-transparent border-none p-0"
                    >
                        Regístrate
                    </button>
                </p>
            </div>
        </div>
    );
}

export default SignInForm;